#using the append method to add to a list/ the added item will move to the last
fruits=["apple","banana","cherry"]
fruits.append("orange")

#insert method to add "lemon" as the second item in the fruits list.
fruits=["apple","banana","cherry"]
fruits.insert(1,"lemon")

#removing an item
fruits=["apple","banana","cherry"]
fruits.remove("banana")

#printing items from the last using negative appearance
fruits=["apple","banana","cherry"]
print(fruits[-1])

#printing a certain range using indexes/(semicolones)
fruits = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(fruits[2:5])

#Use the correct syntax to print the number of items in the list.
